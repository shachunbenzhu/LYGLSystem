package com.cn.ncist.rq.user.vo;

public class UserModel {
	private Integer user_id ;
	private String user_name ;
	private String user_pwd ;
	private String user_repwd ;
	private String user_pocket ;
	private String user_point ;
	private Integer note_id ;
	private String user_touxiang ;
	
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer userId) {
		user_id = userId;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String userName) {
		user_name = userName;
	}
	public String getUser_pwd() {
		return user_pwd;
	}
	public void setUser_pwd(String userPwd) {
		user_pwd = userPwd;
	}
	public String getUser_repwd() {
		return user_repwd;
	}
	public void setUser_repwd(String userRepwd) {
		user_repwd = userRepwd;
	}
	public String getUser_pocket() {
		return user_pocket;
	}
	public void setUser_pocket(String userPocket) {
		user_pocket = userPocket;
	}
	public String getUser_point() {
		return user_point;
	}
	public void setUser_point(String userPoint) {
		user_point = userPoint;
	}
	public String getUser_touxiang() {
		return user_touxiang;
	}
	public void setUser_touxiang(String userTouxiang) {
		user_touxiang = userTouxiang;
	}
	public Integer getNote_id() {
		return note_id;
	}
	public void setNote_id(Integer noteId) {
		note_id = noteId;
	}
}
